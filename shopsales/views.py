                        return JsonResponse({"success": False, "message": f"‡∏™‡∏¥‡∏ô‡∏Ñ‡πâ‡∏≤ {shelf_item.product.product_name} ‡∏Ñ‡∏á‡πÄ‡∏´‡∏•‡∏∑‡∏≠‡∏ö‡∏ô‡∏ä‡∏±‡πâ‡∏ô‡∏ß‡∏≤‡∏á‡πÑ‡∏°‡πà‡∏û‡∏≠"})
